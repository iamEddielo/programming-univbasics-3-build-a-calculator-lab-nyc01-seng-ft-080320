def addition(num1, num2)
  sum = num2 + num1
  puts sum
  sum
end

def subtraction(num1, num2)
  sub = num1 - num2
  puts sub
  sub
end

def division(num1, num2)
  div = num2 / num1
  puts div
  div
end

def multiplication(num1, num2)
  mult = num1 * num2
  puts mult
  mult
end

def modulo(num1, num2)
  mod = num2 % num1
  puts mod
  mod
end

