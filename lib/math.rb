def addition(num1, num2)
  sum = num2 + num1
  puts sum
end

def subtraction(num1, num2)
  sub = num2 - num1
  puts sub
end

def division(num1, num2)
  div = num2 / num1
  puts div
end

def multiplication(num1, num2)
  mult = num1 * num2
  puts mult
end

def modulo(num1, num2)
  mod = num2 % num1
  puts mod
end

def square_root(num)
  square = num.sqrt
  puts square
end